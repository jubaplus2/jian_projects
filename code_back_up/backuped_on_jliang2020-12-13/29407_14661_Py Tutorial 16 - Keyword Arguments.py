def sentence(name = 'Roee', action = 'drinks', item = 'beer'):
  print(name, action, item)

#When nothing is put in parantheses, uses default values  
sentence()
#Cronilogically orders the strings listed for the variables
sentence('Spencer', 'codes', 'well')
#Have to define the variables in order to order them for limited variables
sentence(action = 'is', item = 'awesome')